let multi = function(a,b) { //Declaramos una función "anónima", siempre dentro de una variable.
    return a*b; //Especifícamos la operación y retornamos el resultado.
}



let result = multi(324234,47); //Guardamos el resultado y llamamos a la función(a través de la otra variable "multi") con sus argumentos.
