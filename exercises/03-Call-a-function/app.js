function calculateArea(length, width) { //Definimos una función con sus parámetros.
	return length * width; //Especifícamos la operación y retornamos el resultado.
}

let squareArea1 = calculateArea(4,4); //Guardamos el resultado en una variable y llamamos a la función con diferentes argumentos.
let squareArea2 = calculateArea (2,2);
let squareArea3 = calculateArea (5,5);

console.log(squareArea1); //Imprimimos los resultados.
console.log(squareArea2);
console.log(squareArea3);